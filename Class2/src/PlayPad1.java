
public class PlayPad1 {

	public static void main(String[] args) {
		
		// Incrementers and decrementers 
		int x = 10;
		int y = 10;
		
		x++;
		y--;
		System.out.println("X = " + x);
		System.out.println("Y = " + y);
		
		x += 4;
		y -= 3;
		
		System.out.println("X = " + x);
		System.out.println("Y = " + y);
		
		
		// modulus/remainder
		int value1 = 12;
		int value2 = 15;
		
		int remainder1 = value1 % 3;
		int remainder2 = value2 % 6;
		
		System.out.println(value1 + " % 3 = " + remainder1);
		System.out.println(value2 + " % 6 = " + remainder2);
		
		
		//Logical Operators
		
		
	}
}
